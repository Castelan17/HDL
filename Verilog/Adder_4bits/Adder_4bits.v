module Adder_4bits (
    input wire a1, b1, a2, b2, a3, b3, a4, b4, cin,
    output wire s1, s2, s3, s4, c1, c2, c3, c4
);

//--------------------------------------------------
assign s1 = (a1 ^ b1^ ^ cin);
assign c1 = ((b1&a1) | ((b1|a1) & cin));
//--------------------------------------------------
assign s2 = (a2 ^ b2^ ^ c1);
assign c2 = ((b2&a2) | ((b2|a2) & c1));
//--------------------------------------------------
assign s3 = (a3 ^ b3^ ^ c2);
assign c3 = ((b3&a3) | ((b3|a3) & c2));
//--------------------------------------------------
assign s4 = (a4 ^ b4^ ^ c3);
assign c4 = ((b4&a4) | ((b4|a4) & c3));
//--------------------------------------------------

endmodule